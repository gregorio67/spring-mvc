DROP TABLE DEPLOY_TARGET;

DB NAME :deploy
CREATE TABLE CPU_INFO
(
	 SEQ					INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT , 
     HOST_NAME       		VARCHAR2(50) NOT NULL,
     UPD_DATE               VARCHAR2(20),
     CPU_USED				REAL,
     CPU_FREE				REAL,
     SYS_CPU				REAL,
	 USER_CPU				REAL,				
     CONSTRAINT UN_CPU_INFO unique (HOST_NAME, UPD_DATE)
)

CREATE TABLE MEM_INFO
(
	 SEQ					INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT , 
     HOST_NAME       		VARCHAR2(50) NOT NULL,
     UPD_DATE               VARCHAR2(20),
     MEM_TOTAL				REAL,
     MEM_FREE				REAL,
     MEM_USED				REAL,
	 MEM_RATE				REAL,
	 PAGE_IN				REAL,
	 PAGE_OUT				REAL,
	 SWAP_TOTAL				REAL,
	 SWAP_USED				REAL,
	 SWAP_RATE				REAL,
     CONSTRAINT UN_MEM_INFO unique (HOST_NAME, UPD_DATE)
)
